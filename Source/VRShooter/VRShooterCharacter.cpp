void AVRShooterCharacter::SetupPlayerInputComponent(class UInputComponent* PlayerInputComponent)
{
	// set up gameplay key bindings
	check(PlayerInputComponent);

	// Bind jump events
	PlayerInputComponent->BindAction("Jump", IE_Pressed, this, &ACharacter::Jump);
	PlayerInputComponent->BindAction("Jump", IE_Released, this, &ACharacter::StopJumping);

	// Bind fire event
	PlayerInputComponent->BindAction("Fire", IE_Pressed, this, &AVRShooterCharacter::OnFire);

	// Enable touchscreen input
	EnableTouchscreenMovement(PlayerInputComponent);

	PlayerInputComponent->BindAction("ResetVR", IE_Pressed, this, &AVRShooterCharacter::OnResetVR);

	// Bind movement events
	PlayerInputComponent->BindAxis("MoveForward", this, &AVRShooterCharacter::MoveForward);
	PlayerInputComponent->BindAxis("MoveRight", this, &AVRShooterCharacter::MoveRight);

	// We have 2 versions of the rotation bindings to handle different kinds of devices differently
	// "turn" handles devices that provide an absolute delta, such as a mouse.
	// "turnrate" is for devices that we choose to treat as a rate of change, such as an analog joystick
	PlayerInputComponent->BindAxis("Turn", this, &APawn::AddControllerYawInput);
	PlayerInputComponent->BindAxis("TurnRate", this, &AVRShooterCharacter::TurnAtRate);
	PlayerInputComponent->BindAxis("LookUp", this, &APawn::AddControllerPitchInput);
	PlayerInputComponent->BindAxis("LookUpRate", this, &AVRShooterCharacter::LookUpAtRate);

	PlayerInputComponent->SetTickableWhenPaused(true);
}

void AVRShooterCharacter::Tick(float DeltaTime) {
	health += DeltaTime;
	if (health > maxHealth)
		health = maxHealth;
}

void AVRShooterCharacter::OnFire()
{
	FHitResult hitResult;
	//UPrimitiveComponent* hitComponent;
	AActor* hitActor;
	FDamageEvent damageEvent;
	FVector start, end;
	FCollisionQueryParams CollisionParams;
	CollisionParams.AddIgnoredComponent(GetCapsuleComponent());
	start = FirstPersonCameraComponent->GetComponentLocation() + FirstPersonCameraComponent->GetForwardVector();
	end = (start + FirstPersonCameraComponent->GetForwardVector() * 10000.f);if (GetWorld()->LineTraceSingleByChannel(hitResult, start, end, ECC_Visibility, CollisionParams)) {
		DrawDebugLine(GetWorld(), FP_MuzzleLocation->GetComponentLocation(), hitResult.ImpactPoint, FColor::Green, false, 0.5f, ECC_WorldStatic, 1.f);
		DrawDebugBox(GetWorld(), hitResult.ImpactPoint, FVector(2.f, 2.f, 2.f), FColor::Blue, false, 0.5f, ECC_WorldStatic, 1.f);
		/*hitComponent = hitResult.GetComponent();
		if (hitComponent->IsSimulatingPhysics())
			hitComponent->AddImpulseAtLocation(hitResult.i * 100.f, hitResult.Location);*/
		hitActor = hitResult.GetActor();
		if (hitActor)
			hitActor->TakeDamage(10.f, damageEvent, this->Controller, this);
	}
	else
		DrawDebugLine(GetWorld(), FP_Gun->GetComponentLocation(), end, FColor::Green, false, 0.5f, ECC_WorldStatic, 1.f);

	// try and play the sound if specified
	if (FireSound != NULL)
	{
		UGameplayStatics::PlaySoundAtLocation(this, FireSound, GetActorLocation());
	}

	// try and play a firing animation if specified
	if (FireAnimation != NULL)
	{
		// Get the animation object for the arms mesh
		UAnimInstance* AnimInstance = Mesh1P->GetAnimInstance();
		if (AnimInstance != NULL)
		{
			AnimInstance->Montage_Play(FireAnimation, 1.f);
		}
	}
}

void AVRShooterCharacter::OnResetVR()
{
	UHeadMountedDisplayFunctionLibrary::ResetOrientationAndPosition();
}

void AVRShooterCharacter::BeginTouch(const ETouchIndex::Type FingerIndex, const FVector Location)
{
	if (TouchItem.bIsPressed == true)
	{
		return;
	}
	if ((FingerIndex == TouchItem.FingerIndex) && (TouchItem.bMoved == false))
	{
		OnFire();
	}
	TouchItem.bIsPressed = true;
	TouchItem.FingerIndex = FingerIndex;
	TouchItem.Location = Location;
	TouchItem.bMoved = false;
}

void AVRShooterCharacter::EndTouch(const ETouchIndex::Type FingerIndex, const FVector Location)
{
	if (TouchItem.bIsPressed == false)
	{
		return;
	}
	TouchItem.bIsPressed = false;
}

